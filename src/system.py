class System:
    def __init__(self, name, periapsis, apoapsis, radius, color, rings, ringscolor, systems):
        self.name = name
        self.periapsis = periapsis
        self.apoapsis = apoapsis
        self.radius = radius
        self.color = color
        self.rings = rings
        self.ringscolor = ringscolor
        self.systems = systems
